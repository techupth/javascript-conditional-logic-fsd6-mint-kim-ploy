// Ternary Operator
let lightBulbStatus = "On";

//Start coding here
if (lightBulbStatus == "On" || lightBulbStatus == "Off") {
  lightBulbStatus == "On"
    ? console.log("Light bulb is On.")
    : console.log("Light bulb is Off.");
} else {
  console.log("Please choose the correct input (On/Off)");
}
